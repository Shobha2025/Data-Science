"""9. Data Types in Python
- int: Integer numbers like 5 or -10 – used for counting and indexing.
- float: Decimal numbers like 3.14 – used for precise calculations.
- str: Text data like "Hello" – used for labels and names.
- bool: Boolean values True or False – used in conditions and filters.
- list: Ordered, changeable collection like [1, 2, 3] – used to store multiple items.
- tuple: Ordered, unchangeable collection like (1, 2) – used for fixed data.
- dict: Key-value pairs like {"name": "John"} – used for structured data.
- set: Unordered collection of unique items like {1, 2, 3} – used to remove duplicates."""

# 1. Variables
name = "Shree"
age = 25
height = 5.4

# 2. Print and Type
print(name)          # Output: Shree
print(type(age))     # Output: <class 'int'>

# 3. Lists
fruits = ["apple", "banana", "mango"]
print(fruits[1])     # banana

# 4. Dictionaries
person = {"name": "Suhani", "age": 25}
print(person["name"])    # Suhani
print(person["age"])